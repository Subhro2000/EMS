    @Override
    public void valueChanged(ListSelectionEvent e)
    {
        if(e.getValueIsAdjusting())
        {
            row = tableUsers.getSelectedRow();
            uid = (int) modelUserTable.getValueAt(row, 0);
        }
    }
    
    
