    private void populateUserTable()
    {
        modelUserTable.setRowCount(0);
        
        String fetch = "SELECT Id, Name, User_Name, Mob_No, Email FROM Users WHERE Role = \"E\" ";
        
        try
        {
            PreparedStatement psmt = conn.prepareStatement(fetch);
            
            ResultSet rs = psmt.executeQuery();
            
            while(rs.next())
            {
                Object ar[] = {rs.getInt("ID"),rs.getString("User_Name"),rs.getString("Name"),rs.getString("Mob_No"),rs.getString("Email")};
                modelUserTable.addRow(ar);
            }
            
            
        } catch (SQLException ex)
        {
            Logger.getLogger(DeleteUserPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }

